	  	<h1>One Family's Story <small>- The Benefits of Having a Roommate</small></h1>

		<img class="span6 pull-right" src="img/old-man-photos.jpg">
	  
		<p>My father had lived at a large beautiful retirement home for six months. He was losing his memory quickly and becoming more and more inappropriate for the huge building, the long hallways, the robust social activities and especially the dining service, which offered no assistance. He struggled daily, even hourly, to cover up his dementia. He even ate alone most of the time. My dad was the most gregarious person who loved baseball and a good cold beer at mealtime. He had no beer and no friends, and no one to whom he made sense. He spent most of his time alone in his studio apartment wondering where he was. He was living at the most glamorous place in town and had absolutely no quality of life.</p>


		
<p>Finally the inevitable happened. It came time to move him to a secured Alzheimer’s community nearby. He was going to have to move quickly and availability was limited. Of course I wanted the best for him, a beautiful private room, walking paths, lots of windows...his own furniture.</p>

<p>
The director showed us in to the only available room within ten miles of my home, a large studio with two beds in it. There was a man in the other bed.</p>



	  
	  
<p>"A shared room?" I said, "No way. My father hasn't had a roommate since the army."</p>

<p>The Admissions Director looked at me, and very gently, after a long pause she said, "You know, your father has never lived alone. He lived in his mother's house...then got married to your mom and lived with her for 55 years. Even during the war, in the army, he was never alone. No wonder he is frightened and not doing well. He is probably lonely and looking for an old friend. A shared suite would be perfect for him."</p>


<div class="span2 pull-left" style="padding:10px 10px 0 10px; margin:10px 10px 10px 0; border:1px solid #ccc;">
	  		<p class="lead"><i><b>"A shared room?" I said, "No way. My father hasn't had a roommate since the army."</b></i></p>
	  </div>


<p>I was not going for it. My father had lived in the lap of luxury forever...a large home on a golf course in Florida. I was feeling so guilty and so sad. All that life was coming to this...a roommate in an old folk's home.</p>

<p>Well it was either that shared room or my living room sofa...so the choice was evident.</p>

<p>"It will do for now" I said, "Just until a private suite opens up."</p>

<p>My husband and I took him there the next day. I kept thinking...what will I tell him? How will I explain the other man? What if they hate each other? What if, what if, what if?</p>

<p>Daddy walked into the building, smiled at everyone, shook hands, laughed at a few of his own bad jokes and found the path to 103A.  We had gone the day before and set up his side of the room nicely with all his own things. We urged him in and there was Leroy...the roommate. I held my breath.</p>

<p>"What are you doing here?" Daddy asked him. The rest, as they say, is history.</p>

<p>He got the best care and twice as much of it. Every time the staff went to help Leroy they checked in on Dad. Whenever activities began, Leroy urged my dad to go with him. They talked about the war, complained about their aches and pains, and accused each other of everything from stealing to smoking. They always made sure the other had a sweater and neither was ever late for a meal again. It was perfect. Leroy was the best and last friend my father remembered having.</p>

<p>So what I learned from this very special woman is that there is little to be gained from living in a room by yourself at 85. Companionship is vital to our spirit. We were not put on this earth to be alone. The Admissions Director knew that well before I did. She gave my father the greatest gift; one we all hope for: quality of life until the end.</p> 


	  	
	  	
	  	
	  
	
		
	  		
	  	

    	
    	

	  
